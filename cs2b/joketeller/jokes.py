# Here's a basic one. Note that all strings are represented with double quotes.
print("How do all the oceans say hello to each other?")
input()
print("They wave!")
print("***HAHAHAHAHAHAHAH***")
print()

# Here's another basic one. The strings here are represented with single quotes.
print('What do you call a bear with no teeth?')
input()
print('A gummy bear!')
print('***HARDY HAR HAR!!!***')
print()

# You can use escape characters for contractions/apostrophes
print('You\'ll laugh at this one, I promise!')
print('I\'ll we\'ll that\'s haven\'t.')

# You can do also use escape characters for double quotes.
print("The farmer said, \"That'll do pig. That'll do.\"")

# Here's another escape character: this one prints what follows on a new line.
print("Wait, these aren't jokes at all. \nI'm so sorry about that.")

# What if I want two print statements to be on the same line?
# That's where the keyword parameter "end" comes in.
print("Hi ", end="")
print("Aaron!")

